int get_DWART_AT_string(Dwarf_Half attrnum, const char **string)
{
    num2str_t entry;
    unsigned length = sizeof(DWARF_Id2STR_AT) / sizeof(struct num2str_t);
    for (int i = 0; i < length; i++)
    {
        entry = DWARF_Id2STR_AT[i];
        if (attrnum == entry.attrnum)
        {
            *string = entry.str;
            return 0;
        }
    }
    return -1;
}